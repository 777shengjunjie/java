package day1;


import java.util.Scanner;

public class learning {

    public static void main(String[] args) {
        //Scanner 键盘输入类；System，in代表从键盘输入
        Scanner sc=new Scanner(System.in);

        //获取int，nextInt();获取字符串next()

        int num=sc.nextInt();
        System.out.println(num);


    }
}
